package com.zhouce;

public class index4 {
    public static void main(String[] args){
        int a=24;
        int b=18;
        int c=36;
        int d=27;
        int w=a+b+c+d;
        int x=w/4;
        int y=a-b;
        System.out.println("年龄总和："+w);
        System.out.println("平均年龄："+x);
        System.out.println("年龄差值："+y);
        for(a=24;a>0;a--){
            System.out.println("自减后的年龄："+a);
        }
    }
}
